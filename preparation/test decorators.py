# decorator test
def extractinfo(f):
    def mod(*args, **kwargs):
        print(args[0])
        returnObject = f(*args, **kwargs)
        print('f has ran')
        return returnObject
    return mod

@extractinfo
def func(a):
    return pow(a,a)


